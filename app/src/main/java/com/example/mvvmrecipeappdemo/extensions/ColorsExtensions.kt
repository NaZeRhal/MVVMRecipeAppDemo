package com.example.mvvmrecipeappdemo.extensions

import androidx.compose.material.MaterialTheme
import androidx.compose.runtime.Composable

@Composable
fun primaryColor() = MaterialTheme.colors.primary

@Composable
fun onPrimaryColor() = MaterialTheme.colors.onPrimary

@Composable
fun secondaryColor() = MaterialTheme.colors.secondary

@Composable
fun onSecondaryColor() = MaterialTheme.colors.onSecondary

@Composable
fun surfaceColor() = MaterialTheme.colors.surface

@Composable
fun onSurfaceColor() = MaterialTheme.colors.onSurface

@Composable
fun errorColor() = MaterialTheme.colors.error

@Composable
fun backgroundColor() = MaterialTheme.colors.background

